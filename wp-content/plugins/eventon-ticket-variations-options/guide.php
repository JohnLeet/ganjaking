<h3>How to set up Variations & Options</h3>

<p>Go to event edit page where you want to add Variation and options.</p>

<p>Enable tickets on the ticket and save event post</p>

<p>Once event tickets are enabled on the event, enable ticket variations & options</p>
<p>Before you can start adding variations, create variation types. Once variation types have been created, you can add ticket variations.</p>

<p>You can also add ticket price options from here.</p>

<p><b>More Documentation:</b> http://docs.myeventon.com/documentation/addons/variations-options/</p>

<p><b>Requirements:</b> EventON and Tickets Addon</p>

